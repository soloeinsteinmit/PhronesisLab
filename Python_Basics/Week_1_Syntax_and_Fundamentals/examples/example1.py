# Performing basic arithmetic
a = 25
b = 10

add = a + b # 25 + 10
multiplication = a * b
subtraction = a - b
power1 = a ** 2 # a to the power of 2 = 625
power2 = b ** 3 # b to the power of 3
division = a / b
floor_division = a // b
modulus = a % b

print("a + b -> ", add)
print("a * b -> ", multiplication)
print("a - b -> ", subtraction)
print("a to the power 2 -> ", power1)
print("b to the power 3 -> ", power2)
print("a / b -> ", division)
print("a // b -> ", floor_division)
print("a % b -> ", modulus)
print("80 % 6 -> ", 80 % 6)



